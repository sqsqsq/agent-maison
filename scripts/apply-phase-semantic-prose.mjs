function applyPairs(text) {
  let out = text;
  for (const [from, to] of PAIRS) {
    out = out.split(from).join(to);
  }
  return out;
}

function dedupeMachineTruthSection(text) {
  const marker = '## 机器契约真源（ephemeral plan vs 持久 contracts）';
  const first = text.indexOf(marker);
  if (first < 0) return text;
  const second = text.indexOf(marker, first + marker.length);
  if (second < 0) return text;
  let rest = text.slice(second);
  while (rest.startsWith(marker)) {
    const next = rest.indexOf(marker, marker.length);
    if (next < 0) break;
    rest = rest.slice(next);
  }
  const blockEnd = text.indexOf('\n## ', first + marker.length);
  const singleBlock =
    blockEnd > first
      ? text.slice(first, blockEnd)
      : text.slice(first, first + 280);
  return text.slice(0, first) + singleBlock.trimEnd() + '\n' + text.slice(second).replace(/^(## 机器契约真源[\s\S]*?)(?=\n## |\n*$)/gm, '').replace(new RegExp(`\\n${marker}[\\s\\S]*?(?=\\n## |$)`, 'g'), '');
}

function dedupePlanSkill(text) {
  const marker = '## 机器契约真源（ephemeral plan vs 持久 contracts）';
  const parts = text.split(marker);
  if (parts.length <= 2) return text;
  const body = parts[0];
  const tail = parts[parts.length - 1];
  const block = `${marker}

- **plan.md**：实现计划叙述与契约**草案/来源**；feature 全链路闭环后可归档降级。
- **contracts.yaml / use-cases.yaml**：**机器契约真源**；coding / review / UT / harness 一律优先读取，避免与 plan.md 双源分叉。
`;
  return body + block + tail.replace(/^[\s\S]*?(?=\n*$)/, '').trimEnd() + '\n';
}

function shouldWalkFile(abs) {
  if (!WALK_EXT.has(path.extname(abs))) return false;
  const norm = abs.replace(/\\/g, '/');
  return !WALK_SKIP_PARTS.some((p) => norm.includes(p.replace(/\\/g, '/')));
}

function collectWalkFiles(dir, out = []) {
  if (!fs.existsSync(dir)) return out;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const abs = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      collectWalkFiles(abs, out);
    } else if (shouldWalkFile(abs)) {
      out.push(abs);
    }
  }
  return out;
}

function processFile(rel) {
  const abs = path.join(ROOT, rel);
  if (!fs.existsSync(abs)) {
    console.warn(`skip missing: ${rel}`);
    return;
  }
  let text = fs.readFileSync(abs, 'utf8');
  const before = text;
  text = applyPairs(text);
  if (rel.endsWith('plan/SKILL.md')) {
    text = dedupePlanSkill(text);
  }
  if (text !== before) {
    fs.writeFileSync(abs, text, 'utf8');
    console.log(`updated: ${rel}`);
  }
}

const seen = new Set();
for (const rel of TARGETS) {
  seen.add(path.normalize(rel).replace(/\\/g, '/'));
  processFile(rel);
}

for (const root of WALK_ROOTS) {
  const absRoot = path.join(ROOT, root);
  for (const abs of collectWalkFiles(absRoot)) {
    const rel = path.relative(ROOT, abs).replace(/\\/g, '/');
    if (seen.has(rel)) continue;
    seen.add(rel);
    processFile(rel);
  }
}
